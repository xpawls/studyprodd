-- 왜 뷰(View)를 사용하는가?
--      보안과 편리한 접근을 위해서 
-- 
-- 뷰(Views) 생성과 제거
--      CREATE VIEW  뷰이름  AS  SELECT
--      DROP VIEW 뷰이름
-- @@@@@@@@@@@@@@

select * from vw_getname;

-- emp 테이블에서 deptno=30 사원을 조회하는 뷰, emp_deptno30 를 정의하시오.
create view if not exists vw_deptno30 as
select * from emp where deptno=30;

-- vw_deptno30 를 사용하여 데이터를 출력하시오.
select * from vw_deptno30;

-- 직원 이름과 부서명이 나오는 뷰, vw_emplist를 생성하시오. 조인 사용.
create view vw_emplist as
select emp.ename,dept.DEPTNO 
	from emp inner join dept  on emp.DEPTNO = dept.DEPTNO;


-- vw_emplist 를 사용하여 데이터를 출력하시오.
select * from vw_emplist;


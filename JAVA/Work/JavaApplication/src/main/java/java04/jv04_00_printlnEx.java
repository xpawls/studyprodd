package java04;

public class jv04_00_printlnEx {
    
    public static void main(String[] args){
        
        int i = 10;
        int j = 20;
        int sum = i + j;
        
        System.out.printf("더하기 : i(%d) + j(%d) = %d%n", i, j, sum);
        System.out.println("더하기 : i(" + i + ") + j(" + j + ") = " + sum);
    }
    
}

package java02;

public class jv02_12_ASCII코드 {
    
    public static void main(String[] args){
        int x = '4';
        int y = '5';
        int result = 0;
        
        result =x + y;
        System.out.println( result ); // 출력 : 더하기 = 9
        
        result =x - y;
        System.out.println( result );
        
        result =x * y;
        System.out.println( result );
        
        result =x / y;
        System.out.println( result );
        
        result =x % y;
        System.out.println( result );
    }
    
}

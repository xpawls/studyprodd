--     1. 보안상의 이유로 컬럼을 숨겨야 할 때.
--     2. 간편한 사용
-- ############



-- ############
-- 인덱스는 데이터 검색시 속도 향상을 위해서 사용.
-- 인덱스를 왜 사용하는가?
--   찾는 속도를 빠르게 하기 위해서.
-- 
-- 인덱스 생성 규칙
-- 	where절에서 사용되는 컬럼에는 인덱스 적용을 고려한다..
-- 	where절에서 사용되는 모든 컬럼에 인덱스를 걸라는 말씀은 아닙니다.
--    해당 컬럼 데이터 중복률이 10%이하일 때는 인덱스를 것이 유리하다.
-- 
-- ############


